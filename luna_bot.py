def handle_message(phone, text):
    text = text.strip()
    text_lower = text.lower()

    user = users.get(phone, {"name": "", "fecha_periodo": "", "state": "new"})

    # НОВЫЙ
    if user["state"] == "new":
        users[phone] = {"name": "", "fecha_periodo": "", "state": "asking_name"}
        return "Привет, я *Тайна* 🌙\nТвоя подруга по осознанному циклу.\n\nЯ помогу тебе понять своё тело, эмоции и энергию — в зависимости от твоей фазы.\n\nКак тебя зовут 💜\n\n_Тайна — это про заботу о себе, не медицинская консультация._"

    # ИМЯ
    if user["state"] == "asking_name":
        name = text.capitalize()
        users[phone] = {"name": name, "fecha_periodo": "", "state": "asking_date"}
        return f"Привет {name} 💜\n\nКогда начались твои последние месячные\n\nНапиши дату вот так:\n*15/03/2025*"

    # ДАТА
    if user["state"] == "asking_date":
        parsed = parse_date(text)
        if not parsed:
            return "Хм, не поняла дату 😊\n\nНапиши вот так: *15/03/2025*"
        phase, cycle_day = get_phase(parsed)
        if not phase:
            return "Не смогла рассчитать цикл. Попробуй формат: *15/03/2025*"
        users[phone] = {"name": user["name"], "fecha_periodo": parsed, "state": "active"}
