def get_phase(last_period_str):
    try:
        last_period = datetime.strptime(last_period_str, "%d/%m/%Y").date()
        today = date.today()
        diff = (today - last_period).days
        cycle_day = (diff % 28) + 1
        for phase in phases:
            if phase["days"][0] <= cycle_day <= phase["days"][1]:
                return phase, cycle_day
        return phases[3], cycle_day
    except:
        return None, None

def parse_date(text):
    for fmt in ["%d/%m/%Y", "%d-%m-%Y", "%d/%m/%y", "%d %m %Y"]:
        try:
            return datetime.strptime(text.strip(), fmt).strftime("%d/%m/%Y")
        except:
            continue
    return None

def get_forecast(last_period_str):
    try:
        last_period = datetime.strptime(last_period_str, "%d/%m/%Y").date()
        today = date.today()
        lines = ["🔮 Твои следующие 5 дней\n"]
        labels = ["Завтра", "Через 2 дня", "Через 3 дня", "Через 4 дня", "Через 5 дней"]
        for i in range(1, 6):
            future = today + timedelta(days=i)
            diff = (future - last_period).days
            cycle_day = (diff % 28) + 1
            for phase in phases:
                if phase["days"][0] <= cycle_day <= phase["days"][1]:
                    lines.append(f"{phase['emoji']} {labels[i-1]} — День {cycle_day} · {phase['name']}")
                    break
        lines.append("\n_Планируй неделю по своему циклу 💜_")
        return "\n".join(lines)
    except:
        return "Не смогла рассчитать прогноз."

def send_reply(text):
    resp = MessagingResponse()
    resp.message(text)
    return str(resp)

