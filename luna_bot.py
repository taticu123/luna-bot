        # ДОБРОЕ УТРО
        if any(w in text_lower for w in ["доброе утро", "привет", "хай", "утро", "добрый день", "добрый вечер"]):
            return morning_message(phase, cycle_day)

        # СЕГОДНЯ — навигация
