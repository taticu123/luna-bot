def get_forecast(last_period_str, phase_now, cycle_day_now):
    try:
        last_period = datetime.strptime(last_period_str, "%d/%m/%Y").date()
        today = date.today()
        lines = ["🔮 Твои следующие 5 дней\n"]
        labels = ["Завтра", "Через 2 дня", "Через 3 дня", "Через 4 дня", "Через 5 дней"]
        for i in range(1, 6):
            future = today + timedelta(days=i)
            diff = (future - last_period).days
            cycle_day = (diff % 28) + 1
            for phase in phases:
                if phase["days"][0] <= cycle_day <= phase["days"][1]:
                    hint = ""
                    if phase["key"] != phase_now["key"]:
                        hint = f" — {phase['forecast_hint']}"
                    lines.append(f"{phase['emoji']} {labels[i-1]}: день {cycle_day}, {phase['name']}{hint}")
                    break
        lines.append(f"\n{phase_now['forecast_hint']}")
        lines.append("\n_Планируй неделю зная свой ритм 💜_")
        return "\n".join(lines)
    except:
        return "Не смогла рассчитать прогноз."

